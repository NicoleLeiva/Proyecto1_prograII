    private void btnBloquearActionPerformed(java.awt.event.ActionEvent evt) {
        String usuarioABloquear = txtUser.getText().trim();

        if (usuarioABloquear.isEmpty()) {
            System.out.println("Seleccione un usuario para bloquear");
            return;
        }

        bloquearUsuario(usuarioABloquear);
    }

    private void bloquearUsuario(String nombre) {
        if (nombre == null || nombre.isEmpty()) {
            return;
        }

        // A√±adimos usuario a la lista de bloqueados (debe existir en Servidor)
        Servidor.usuariosBloqueados.add(nombre);
        System.out.println("Usuario bloqueado: " + nombre);

        // Opcional: cerramos conexi√≥n para aplicar bloqueo inmediato
        cerrarConexionUsuario(nombre);
        // Actualizamos la lista de usuarios
        inicialiceLista();
    }

